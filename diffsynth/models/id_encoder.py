# --------------------------------------------------------------------------------
# 注意：这里的Attention模块需要和您项目中的定义保持一致。
# 从您提供的 `wan_video_dit.py` 文件来看，Attention模块似乎比较复杂，
# 且支持FlashAttention。为了简单起见，这里我们使用一个标准的Attention实现。
# 如果需要优化，可以替换为您项目中`flash_attention`的封装。
